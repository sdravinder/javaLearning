package basics.operators;

public class RelationalOperators {

    public static void main(String[] args) {

        //Relational Operators
        int firstNumber = 12;
        int secondNumber = 16;

        // ==
        System.out.println(firstNumber == secondNumber);

        // !=
        System.out.println(firstNumber != secondNumber);

        // >
        System.out.println(firstNumber > secondNumber);

        // <
        System.out.println(firstNumber < secondNumber);

        // >=
        System.out.println(firstNumber >= secondNumber);

        // <=
        System.out.println(firstNumber <= secondNumber);
    }
}
